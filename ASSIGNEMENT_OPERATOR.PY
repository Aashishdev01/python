# Assignment: =
a = 10
print(a)
# Addition assignment: +=

# a = a + 10
a += 10
print(a)

# Subtraction assignment: -=
a -= 10
print(a)
# Multiplication assignment: *=
a *= 10
print(a)
# Division assignment: /=
a /= 10
print(a)
# Modulo assignment: %=
a %= 10
print(a)
# Exponentiation assignment: **=
a **= 10
print(a)
# Floor division assignment: //=
a //= 10
print(a)
